    private boolean Abertura() {
         int value = this.numero+1;
        System.out.println("numero: "+value+" iduser: "+this.id_utilizador);
       
        liga.conexao();
        try {
            double val = Double.parseDouble(this.jTextField1.getText());
            PreparedStatement pst = liga.con.prepareStatement("insert into turno "
                    + " (idutilizador, saldo_inicial,  saldo_final, saldo_atual,  "
                    + "hora_entrada , data_entrada,  "
                    + "numero ,  estado ,  fatura_recibo,  fatura , "
                    + "recibo , nota_de_credito, saida_de_caixa ,"
                    + "entrada_em_caixa, numerario,  bancos,nota_de_pagamento) "
                    + "VALUES ("+this.id_utilizador+", "+val+",  0, "+val+", current_time , current_date, "
                    +""+value+",  true ,  0,  0 , 0 , 0, 0 ,"
                    + "0, 0,  0, 0)" );
            
            pst.execute();
            
            pst = liga.con.prepareStatement("UPDATE caixa set entrada = entrada + " + val + ", saldo = saldo + " + val + ", idempresa =" + 1);
            boolean execute = pst.execute();

                
//            JOptionPane.showMessageDialog(null, "Dados Inseridos com sucesso.", "Guardado.", JOptionPane.INFORMATION_MESSAGE);
            liga.deconecta();
            return true;

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Dados não Inseridos."+ex, "Erro ", JOptionPane.WARNING_MESSAGE);
        }
        liga.deconecta();
        return false;
    }

