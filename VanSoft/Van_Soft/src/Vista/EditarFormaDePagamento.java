    public boolean UpdateFormaDePagamento() {

        liga.conexao();
        try {

            PreparedStatement pst = liga.con.prepareStatement("UPDATE forma_de_pagamento SET descricao= ? WHERE idformadepagamento = "+this.idForma);
            pst.setString(1, this.jTextField_nome.getText());
            
            pst.execute();
            JOptionPane.showMessageDialog(null, "Dados Inseridos com sucesso.", "Guardado.", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {

            JOptionPane.showMessageDialog(null, ex, "Dados não inderidos", JOptionPane.WARNING_MESSAGE);
        }
        liga.deconecta();

        return false;
    }

